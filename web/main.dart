import 'stage/stage.dart';


class Game {
  // The main game object, keeps track of global variables
  // The main viewport everything should draw to
  Viewport view;
  Stage stage;
  
  Game(mdata,canvas) {
    view = new Viewport(canvas);
    stage = new Stage(mdata, view);
    
    // Initialize mouse and keyboard interaction
    Keyboard.init();
    Mouse.init();
    
    // start the main game loop
    window.requestAnimationFrame(loop);
  }
  
  // The main game loop
  void loop(num time) {
    // update the stage
    stage.update();
    
    // update the viewport
    view.update();
    // note currently does not clear the screen
    // draw the stage
    stage.draw();
    
    // ask for the next cycle
    window.requestAnimationFrame(loop);
  }
}

